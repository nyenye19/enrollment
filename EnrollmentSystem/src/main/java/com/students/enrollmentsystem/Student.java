    private void txtBdayFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtBdayFocusLost
        // TODO add your handling code here:
        ZoneId z = ZoneId.of("Asia/Manila");
        LocalDate today = LocalDate.now(z);  // Always pass a time zone.

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("M/d/y", Locale.ENGLISH);
        LocalDate date = LocalDate.parse(txtBday.getText(), formatter);

        int age = getDifferenceDays(today, date);

        txtAge.setText(String.valueOf(age));
    }//GEN-LAST:event_txtBdayFocusLost

    private void txtStudentIdKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtStudentIdKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == 10) {  //ENTER
            if (this.getTitle().compareTo("Edit Student") == 0 || 
                 this.getTitle().compareTo("Remove Student") == 0 ) {
                Document doc = database.getCollection("students").find(eq("studid", txtStudentId.getText())).first();
                txtFullname.setText(doc.get("fullname").toString());
                txtBday.setText(doc.get("birthday").toString());
                txtAge.setText(doc.get("age").toString());
                txtAddress.setText(doc.get("address").toString());
                if(doc.get("gender").toString().equals("M")){
                    rbnMale.setSelected(true);
                }
                else {
                    rbnFemale.setSelected(true);
                }
            }
        }
    }//GEN-LAST:event_txtStudentIdKeyPressed

