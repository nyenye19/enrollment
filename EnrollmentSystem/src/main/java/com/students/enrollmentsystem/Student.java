    public int getDifferenceDays(LocalDate d1, LocalDate d2) {
        int diff = d1.getYear() - d2.getYear();
        return diff;
    }

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

        //Preparing a document
        Document document = new Document();

        String fullname = txtFullname.getText();

        String studid = txtStudentId.getText();
        String birthday = txtBday.getText();
        String age = txtAge.getText();
        String address = txtAddress.getText();
        String gender;
        if (rbnMale.isSelected()) {
            gender = "M";
        } else {
            gender = "F";
        }
        if (this.getTitle().compareTo("Add Student") == 0) {
            document.append("studid", studid);

            document.append("fullname", fullname);
            document.append("birthday", birthday);
            document.append("address", address);
            document.append("age", age);
            document.append("gender", gender);

            database.getCollection("students").insertOne(document);
        } else if (this.getTitle().compareTo("Edit Student") == 0) {
            Bson updates = Updates.combine(
                    Updates.set("fullname",fullname),
                    Updates.set("address",address),
                    Updates.set("address", address),
                    Updates.set("birtday", birthday),
                    Updates.set("age", age),
                    Updates.set("gender", gender)
            );
            database.getCollection("students").updateOne(Filters.eq("studid", studid), updates);
        } else if (this.getTitle().compareTo("Remove Student") == 0) {
            database.getCollection("students").deleteOne( Filters.eq("studid", studid) );
        }

        JOptionPane.showMessageDialog(null, "Student record saved");

        this.dispose();

    }//GEN-LAST:event_btnSaveActionPerformed

